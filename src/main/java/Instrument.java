public abstract class Instrument {

    String brand;
    String model;
    double buyPrice;
    double sellPrice;

    public String getBrand() {
        return brand;
    }

    public String getModel() {
        return model;
    }

    public double getBuyPrice() {
        return buyPrice;
    }

    public double getSellPrice() {
        return sellPrice;
    }
}
